    public boolean verify(){
        String modle =jTextFieldmodel.getText();
        String clas=jTextFieldclass.getText();
        if(modle.trim().equals("") || clas.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Enter all the fields");
            return false;
        }else{
            return true;
        }        
    }
    
