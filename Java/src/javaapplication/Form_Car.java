    private void jsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jsearchActionPerformed
        // TODO add your handling code here:
        int carid=(int)jspinid.getValue();
        classes.Car newcar=Cars.getCarById(carid);
        if(newcar==null){
            JOptionPane.showMessageDialog(null,"No car with this id.");
        }else{
            jTextFieldclass.setText(newcar.getClas());
            jTextFieldmodel.setText(newcar.getModel());            
            jspinpassno.setValue(newcar.getPassengers());
            jspinprice.setValue(newcar.getPrice());            
            jComboBoxbrand.setSelectedItem(newcar.getBrand());
            jComboBoxfuel.setSelectedItem(newcar.getFuel());
            jComboBoxcolor.setSelectedItem(newcar.getColor());
            if(newcar.getGearbox().equals("manual")){jgearman.setSelected(true);}
            if(newcar.getAri_cond().equals("yes")){jac.setSelected(true);}
            if(newcar.getAirbag().equals("yes")){jairbags.setSelected(true);}
            if(newcar.getSunroof().equals("yes")){jsun.setSelected(true);}
            if(newcar.getHeatedseat().equals("yes")){jseats.setSelected(true);}
            if(newcar.getGps().equals("yes")){jgps.setSelected(true);}
            if(newcar.getBluetooth().equals("yes")){jbluetooth.setSelected(true);}
            if(newcar.getNavigation().equals("yes")){jnav.setSelected(true);}            
        }                
    }//GEN-LAST:event_jsearchActionPerformed

