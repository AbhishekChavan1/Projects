    public void datatablewithcar(){
        ArrayList<classes.Car> custlists=car.carbrandlist(id);
        String[] columnName={"ID","Model","Fuel","Color","Price"};
        Object[][] rows=new Object[custlists.size()][columnName.length];
        
        for(int i=0;i<custlists.size();i++){
            rows[i][0]=custlists.get(i).getId();
            rows[i][1]=custlists.get(i).getModel();            
            rows[i][2]=custlists.get(i).getFuel();            
            rows[i][3]=custlists.get(i).getColor();   
            rows[i][4]=custlists.get(i).getPrice();   
        }
        DefaultTableModel model=new DefaultTableModel(rows,columnName);
        jTablecars.setModel(model);        
    }     
    
